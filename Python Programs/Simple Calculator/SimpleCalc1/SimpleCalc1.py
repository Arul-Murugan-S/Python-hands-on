class Calculator():
    def init(self):
        pass
    
    def addition(self,a,b):
        add=a+b
        return add
    def substraction(self,a,b):
        sub=a-b
        return sub
    def multiplication(self,a,b):
        mul=a*b
        return mul
    def division(self,a,b):
        div=a/b
        return div


